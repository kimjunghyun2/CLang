#include<stdio.h>
int main(void) {
	int val1 = 5, val2 = 2;

	printf("%d + %d=%d\n", val1, val2, val1 + val2);
	printf("%d - %d=%d\n", val1, val2, val1 - val2);
	printf("%d * %d=%d\n", val1, val2, val1 * val2);
	printf("%d / %d=%d\n", val1, val2, val1 / val2);
	printf("%d %% %d=%d\n", val1, val2, val1 % val2);


	return 0;
}
